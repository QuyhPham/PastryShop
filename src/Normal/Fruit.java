package Normal;

public class Fruit {
	public String Fruit;
	public long Price; 
	public int IDf; 
	
	public Fruit() {
		
	}

	

	public Fruit(String fruit, long price, int iDf) {
		
		Fruit = fruit;
		Price = price;
		IDf = iDf;
	}



	public String getFruit() {
		return Fruit;
	}

	public void setFruit(String fruit) {
		Fruit = fruit;
	}

	public long getPrice() {
		return Price;
	}

	public void setPrice(long price) {
		Price = price;
	}

	public int getIDf() {
		return IDf;
	}

	public void setIDf(int iDf) {
		IDf = iDf;
	}
	
	
	
	

	
	
	
	
	

}